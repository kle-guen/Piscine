#include "prototype.h"

int	**ft_32_row(int **tab)
{
	int	y;

	y = 0;
	while (y < 6)
	{
		if (tab[0][y] == 3 && tab[5][y] == 2)
			tab[3][y] = 4;
		if (tab[5][y] == 3 && tab[0][y] == 2)
			tab[2][y] = 4;
		y++;
	}	
	return (tab);
}

int	**ft_32_col(int **tab)
{
	int	x;

	x = 0;
	while (x < 6)
	{
		if (tab[x][0] == 3 && tab[x][5] == 2)
			tab[x][3] = 4;
		if (tab[x][5] == 3 && tab[x][0] == 2)
			tab[x][2] = 4;
		x++;
	}	
	return (tab);
}
