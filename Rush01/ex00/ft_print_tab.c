#include <unistd.h>
#include "prototype.h"

void	ft_print_tab(int **tab)
{
	int		x;
	int		y;
	char	c;

	x = 1;
	y = 1;
	while (y >= 1 && y <= 4)
	{
		while (x >= 1 && x <= 4)
		{
			c = tab[x][y] + 48;
			write(1, &c, 1);
			write(1, " ", 1);
			x++;
		}
		write(1, "\n", 1);
		y++;
		x = 1;
	}
}
